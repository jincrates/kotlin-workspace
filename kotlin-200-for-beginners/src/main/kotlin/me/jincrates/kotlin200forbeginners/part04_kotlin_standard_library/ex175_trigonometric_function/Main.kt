package me.jincrates.kotlin200forbeginners.part04_kotlin_standard_library.ex175_trigonometric_function;

import kotlin.math.*

/*
175. 삼각함수
 */
fun main(args:Array<String>): Unit {
    println(sin(PI / 4))
    println(cos(PI / 4))
    println(tan(PI / 4))

    println(asin(PI / 4))
    println(acos(PI / 4))
    println(atan(PI / 4))


    println(sinh(PI / 4))
    println(cosh(PI / 4))
    println(tanh(PI / 4))


    println(asinh(PI / 4))
    println(acosh(PI / 4))
    println(atanh(PI / 4))
}